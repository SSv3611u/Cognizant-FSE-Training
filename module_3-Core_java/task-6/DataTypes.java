class DataTypes{
    void main(){
        int a = 10;
        float b = 10f;
        double c = 10.0;
        char d = 'a';
        boolean f = true;

        System.out.println("Integer: "+a);
        System.out.println("Floating number: "+b);
        System.out.println("Double: "+c);
        System.out.println("Character: "+d);
        System.out.println("Boolean: "+f);
    }
}